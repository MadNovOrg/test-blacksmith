UPDATE organization
SET address = jsonb_set(
    address,
    '{countryCode}',
    (SELECT iso_code
     FROM country_codes
     WHERE country_codes.country_name = address ->> 'country')::jsonb,
    true
)
WHERE address ? 'country'
  AND address ->> 'country' IS NOT NULL
  AND (
    NOT address ? 'countryCode'
    OR address ->> 'countryCode' IS NULL
  )
  AND address ->> 'country' IN (
    SELECT country_name
    FROM country_codes
  );

DROP TABLE IF EXISTS country_codes;